# There can be multiple functions.
def helloworld():  # Function 1
    print("Hello Python")
    c = 10 + 90
    print(c)
    print("Bye")


def summ(num1, num2):  # Function 2
    result = num1 + num2  # Doing arithmetic operation here.
    print("The result is :", result)


helloworld()
summ(10, 30)     # We can call function name randomly.
