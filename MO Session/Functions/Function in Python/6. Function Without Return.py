def summ(num1, num2):
    result = num1 + num2
    print("The result is :", result)


# When we call summ() function with print() then, it will result NONE. Because the sum function is not return anything.
# The following code snippet returns NONE.
print(summ(10, 20))

# So using return keyword, we can make it to return something in function call.
