def get_sum_of_all_numbers(*args):
    print("The sum of all numbers:", sum(args))  # Sum is built in function.


def get_min_of_all_numbers(*args):
    print("The minimum number of all numbers: ", min(args))


def get_max_of_all_numbers(*args):
    print("The Maximum number of all number: ", max(args))


get_sum_of_all_numbers(1, 3, 5, 7, 9, 2, 4, 6, 8, 10)
get_min_of_all_numbers(1, 3, 5, 7, 9, 2, 4, 6, 8, 10)
get_max_of_all_numbers(1, 3, 5, 7, 9, 2, 4, 6, 8, 10)
