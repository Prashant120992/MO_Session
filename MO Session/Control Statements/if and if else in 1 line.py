print("***********if condition in one line**********")
if 15 > 10: print("Yes")

print("***********If else condition in one line*******")
print("Yes") if 100 < 10 else print("No")
# The Left-hand side only executes if the condition (if 100<10) evaluates true.
# Otherwise, the right-hand side executes.

print("*********** Example 1 : If else condition in one line*******")
mark = 95
print("A+") if mark < 90 else print("A")
